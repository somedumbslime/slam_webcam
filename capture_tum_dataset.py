from pathlib import Path
from datetime import datetime
import argparse
import time

import cv2


def parse_args():
    parser = argparse.ArgumentParser(
        description="Capture webcam sequence in TUM-like format for ORB-SLAM3 mono_tum."
    )
    parser.add_argument(
        "--camera",
        type=int,
        default=0,
        help="Camera index (default: 0).",
    )
    parser.add_argument(
        "--out-dir",
        type=str,
        default="datasets",
        help="Base output directory (default: datasets).",
    )
    parser.add_argument(
        "--name",
        type=str,
        default=None,
        help="Sequence name. If omitted, autogenerated.",
    )
    parser.add_argument(
        "--fps",
        type=float,
        default=15.0,
        help="Target save FPS (default: 15.0).",
    )
    parser.add_argument(
        "--width",
        type=int,
        default=640,
        help="Requested camera width (default: 640).",
    )
    parser.add_argument(
        "--height",
        type=int,
        default=480,
        help="Requested camera height (default: 480).",
    )
    parser.add_argument(
        "--warmup-sec",
        type=float,
        default=1.0,
        help="Warmup time before recording starts (default: 1.0).",
    )
    return parser.parse_args()


def choose_sequence_dir(base_dir: Path, name: str | None):
    if name is None or name.strip() == "":
        stamp = datetime.now().strftime("webcam_%Y%m%d_%H%M%S")
        seq_name = stamp
    else:
        seq_name = name.strip()

    seq_dir = base_dir / seq_name
    img_dir = seq_dir / "images"
    img_dir.mkdir(parents=True, exist_ok=True)
    return seq_dir, img_dir


def main():
    args = parse_args()

    base_dir = Path(args.out_dir)
    seq_dir, img_dir = choose_sequence_dir(base_dir, args.name)

    cap = cv2.VideoCapture(args.camera)
    if not cap.isOpened():
        raise RuntimeError(f"Cannot open camera index {args.camera}")

    cap.set(cv2.CAP_PROP_FRAME_WIDTH, int(args.width))
    cap.set(cv2.CAP_PROP_FRAME_HEIGHT, int(args.height))

    actual_w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    actual_h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    target_fps = max(0.5, float(args.fps))
    save_period = 1.0 / target_fps

    print("Recording TUM-like sequence. Press 'q' to stop.")
    print(f"Output sequence: {seq_dir.resolve()}")
    print(f"Target FPS: {target_fps:.2f}")
    print(f"Camera resolution: {actual_w}x{actual_h}")

    if args.warmup_sec > 0:
        warmup_end = time.perf_counter() + float(args.warmup_sec)
        while time.perf_counter() < warmup_end:
            ok, _ = cap.read()
            if not ok:
                break

    start = time.perf_counter()
    next_save_t = start
    rows = []
    saved = 0

    try:
        while True:
            ok, frame = cap.read()
            if not ok:
                print("Failed to read frame from camera.")
                break

            now = time.perf_counter()
            elapsed = now - start

            if now >= next_save_t:
                ts = elapsed
                filename = f"{ts:.6f}.png"
                file_path = img_dir / filename
                cv2.imwrite(str(file_path), frame)
                rows.append((ts, f"images/{filename}"))
                saved += 1
                next_save_t += save_period

            vis = frame.copy()
            cv2.putText(
                vis,
                f"saved={saved} time={elapsed:.2f}s fps_target={target_fps:.1f}",
                (12, 30),
                cv2.FONT_HERSHEY_SIMPLEX,
                0.7,
                (0, 255, 0),
                2,
                cv2.LINE_AA,
            )
            cv2.imshow("Capture TUM Dataset (q to stop)", vis)
            if cv2.waitKey(1) & 0xFF == ord("q"):
                break
    finally:
        cap.release()
        cv2.destroyAllWindows()

    if saved < 2:
        raise RuntimeError("Too few frames saved. Record at least 2 frames.")

    rgb_path = seq_dir / "rgb.txt"
    with rgb_path.open("w", encoding="utf-8") as f:
        f.write("# color images\n")
        f.write("# timestamp filename\n")
        for ts, rel in rows:
            f.write(f"{ts:.6f} {rel}\n")

    info_path = seq_dir / "sequence_info.txt"
    with info_path.open("w", encoding="utf-8") as f:
        f.write("Webcam TUM-like sequence metadata\n")
        f.write(f"saved_frames: {saved}\n")
        f.write(f"target_fps: {target_fps:.6f}\n")
        f.write(f"camera_resolution: {actual_w}x{actual_h}\n")
        f.write(f"duration_sec: {rows[-1][0]:.6f}\n")
        f.write("rgb_file: rgb.txt\n")

    print("Done.")
    print(f"Saved frames: {saved}")
    print(f"Sequence dir: {seq_dir.resolve()}")
    print(f"TUM rgb list: {rgb_path.resolve()}")


if __name__ == "__main__":
    main()
